// 1. Arithmetic Operators
let x;

x = 5 + 5;
console.log('5 + 5 =', x);

x = 5 - 5;
console.log('5 - 5 =', x);

x = 5 * 5;
console.log('5 * 5 =', x);

x = 5 / 5;
console.log('5 / 5 =', x);

x = 7 % 5;
console.log('7 % 5 =', x);

// Concatenation
x = 'Hello' + ' ' + 'World';
console.log('Concatenation:', x);

// Exponent
x = 2 ** 3;
console.log('2 ** 3 =', x);

// Increment
x = 1;
console.log('Initial x:', x);
x++;
console.log('After x++:', x);

// Decrement
x--;
console.log('After x--:', x);

// 2. Assignment Operators
x = 10;
console.log('Initial x =', x);

x += 5;
console.log('x += 5 →', x);

x -= 5;
console.log('x -= 5 →', x);

x *= 5;
console.log('x *= 5 →', x);

x /= 5;
console.log('x /= 5 →', x);

x %= 5;
console.log('x %= 5 →', x);

x **= 5;
console.log('x **= 5 →', x);

// 3. Comparison Operators
console.log('2 == "2" →', 2 == '2');
console.log('2 === "2" →', 2 === '2');
console.log('2 != "2" →', 2 != '2');
console.log('2 !== 2 →', 2 !== 2);
console.log('10 > 5 →', 10 > 5);
console.log('10 < 5 →', 10 < 5);
console.log('10 <= 5 →', 10 <= 5);
console.log('10 >= 5 →', 10 >= 5);
